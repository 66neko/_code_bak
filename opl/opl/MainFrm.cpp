BOOL CMainFrame::PreCreateWindow(CREATESTRUCT& cs)
{
	if( !CFrameWnd::PreCreateWindow(cs) )
		return FALSE;
	// TODO: 在此处通过修改
	//  CREATESTRUCT cs 来修改窗口类或样式
	cs.hMenu = NULL;
	cs.cx = 800;
	cs.cy = 600;
	cs.style = WS_OVERLAPPED | WS_CAPTION | WS_THICKFRAME //使窗口不能用鼠标改变大小
		 | WS_MINIMIZEBOX | WS_SYSMENU;
	//cs.style &= ~WS_BORDER;

	return TRUE;
}


